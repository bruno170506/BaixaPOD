package br.com.baixapod.model;

public class ItemPOD {

	private String n_hawb;
	private String nome_desti;
	private String rua_desti;
	private String numero_desti;
	private String bairro_desti;
	private String cidade_desti;
	private String n_tentativas;
	private String ocorrencia;
	private String sucesso;
	
	public String getN_hawb() {
		return n_hawb;
	}
	public void setN_hawb(String n_hawb) {
		this.n_hawb = n_hawb;
	}
	public String getNome_desti() {
		return nome_desti;
	}
	public void setNome_desti(String nome_desti) {
		this.nome_desti = nome_desti;
	}
	public String getRua_desti() {
		return rua_desti;
	}
	public void setRua_desti(String rua_desti) {
		this.rua_desti = rua_desti;
	}
	public String getNumero_desti() {
		return numero_desti;
	}
	public void setNumero_desti(String numero_desti) {
		this.numero_desti = numero_desti;
	}
	public String getBairro_desti() {
		return bairro_desti;
	}
	public void setBairro_desti(String bairro_desti) {
		this.bairro_desti = bairro_desti;
	}
	public String getCidade_desti() {
		return cidade_desti;
	}
	public void setCidade_desti(String cidade_desti) {
		this.cidade_desti = cidade_desti;
	}
	public String getN_tentativas() {
		return n_tentativas;
	}
	public void setN_tentativas(String n_tentativas) {
		this.n_tentativas = n_tentativas;
	}
	public String getOcorrencia() {
		return ocorrencia;
	}
	public void setOcorrencia(String ocorrencia) {
		this.ocorrencia = ocorrencia;
	}

	public String getSucesso() {
		return sucesso;
	}
	public void setSucesso(String sucesso) {
		this.sucesso = sucesso;
	}
}
